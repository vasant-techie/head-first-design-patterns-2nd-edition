package headfirst.design_patterns.behavioral.command.problem;

public class CeilingLight {
    public void on() {
        System.out.println("CeilingLight is ON.");
    }
    
    public void off() {
        System.out.println("CeilingLight is OFF.");
    }

    public void dim() {
        System.out.println("CeilingLight is DIM.");
    }

}
